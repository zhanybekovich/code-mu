"use strict";

// increment postfix
let num = 0;
console.log(num++);
console.log(num);

// increment prefix
let num2 = 0;
console.log(++num2);
console.log(num2);

// decrement postfix
let num3 = 5;
console.log(num3--);
console.log(num3);

// decrement prefix
let num4 = 50;
console.log(--num4);
console.log(num4);
